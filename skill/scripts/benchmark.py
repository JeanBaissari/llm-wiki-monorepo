#!/usr/bin/env python3
"""benchmark.py — Generate synthetic wikis at various sizes and benchmark llm-wiki tool performance.

Usage:
    python3 benchmark.py <output-csv>

Example:
    python3 benchmark.py /tmp/benchmark-results.csv

Measures wall time for lint, graph build, graph insights (Node), and Python graph insights
across synthetic wikis with 10, 100, 500, 1000, and 5000 pages.  Outputs CSV and summary."""

import csv
import json
import math
import shutil
import subprocess
import sys
import time
from pathlib import Path


REPO_DIR = Path(__file__).resolve().parent.parent.parent
GRAPH_ENGINE = REPO_DIR / "graph-engine" / "dist" / "index.js"
LINT_SCRIPT = REPO_DIR / "skill" / "scripts" / "lint_wiki.py"
INSIGHTS_SCRIPT = REPO_DIR / "skill" / "scripts" / "graph_insights.py"

WORKSPACE = Path("/tmp/llm-wiki-benchmark")

SIZES = [10, 100, 500, 1000, 5000]
PAGE_TYPES = ["concepts", "entities", "summaries"]

LIPSUM = (
    "Lorem ipsum dolor sit amet, consectetur adipiscing elit. "
    "Sed do eiusmod tempor incididunt ut labore et dolore magna aliqua. "
    "Ut enim ad minim veniam, quis nostrud exercitation ullamco laboris "
    "nisi ut aliquip ex ea commodo consequat."
)

HEADINGS = [
    "# Introduction",
    "## Overview",
    "## Key Points",
    "### Detail A",
    "### Detail B",
    "## Related Work",
    "## Analysis",
    "## Conclusion",
]


def generate_page(title: str, page_type: str, existing_pages: list[str], idx: int) -> str:
    created = f"2025-{(idx % 12) + 1:02d}-{(idx % 28) + 1:02d}"
    updated = f"2026-{(idx % 12) + 1:02d}-{(idx % 28) + 1:02d}"
    tags_list = sorted({page_type, f"topic-{idx % 7}", f"area-{idx % 4}"})
    sources_list = ["source-a", "source-b"]

    wikilinks = []
    if existing_pages:
        num_links = min(len(existing_pages), max(1, (idx * 9973) % 6))
        for li in range(num_links):
            target_idx = ((idx + 1) * (li + 1) * 7919) % len(existing_pages)
            wikilinks.append(existing_pages[target_idx])

    heading = HEADINGS[idx % len(HEADINGS)]
    body_paras = []
    for pi in range(3):
        para = LIPSUM[:80 + (idx * 17 + pi * 31) % 120]
        if wikilinks and (pi + idx) % 2 == 0:
            link = wikilinks[(pi + idx) % len(wikilinks)]
            para += f" See [[{link}]] for more details."
        body_paras.append(para)

    link_lines = "\n".join(f"- [[{link}]]" for link in wikilinks) if wikilinks else "- No related pages"

    frontmatter = (
        "---\n"
        f'title: "{title}"\n'
        f"type: {page_type}\n"
        f"created: {created}\n"
        f"updated: {updated}\n"
        f"sources: {json.dumps(sources_list)}\n"
        f"tags: {json.dumps(tags_list)}\n"
        "---\n"
    )

    body = (
        f"\n{heading}\n\n"
        f"{body_paras[0]}\n\n"
        f"## Details\n\n"
        f"{body_paras[1]}\n\n"
        f"**Important:** {body_paras[2]}\n\n"
        f"## Related Pages\n\n"
        f"{link_lines}\n"
    )

    return frontmatter + body


def scaffold_wiki(wiki_root: Path):
    dirs = [
        "raw/articles",
        "raw/papers",
        "raw/notes",
        "raw/refs",
        "wiki/concepts",
        "wiki/entities",
        "wiki/summaries",
        "wiki/comparisons",
        "wiki/graphs",
        "wiki/synthesis",
        "outputs/queries",
        "log",
        "audit",
        "audit/resolved",
    ]
    for d in dirs:
        (wiki_root / d).mkdir(parents=True, exist_ok=True)

    index_md = (
        "# Benchmark Wiki\n\n"
        "## Concepts\n\n"
        "*(generated by benchmark.py)*\n\n"
        "## Entities\n\n"
        "*(generated by benchmark.py)*\n\n"
        "## Summaries\n\n"
        "*(generated by benchmark.py)*\n\n"
    )
    (wiki_root / "wiki" / "index.md").write_text(index_md, encoding="utf-8")
    (wiki_root / "CLAUDE.md").write_text("# Benchmark Wiki\n\nSchema placeholder.\n", encoding="utf-8")
    (wiki_root / "log" / "20260622.md").write_text("# 2026-06-22\n\n## [00:00] benchmark | Generated wiki\n", encoding="utf-8")
    (wiki_root / "audit" / ".gitkeep").write_text("")
    (wiki_root / "audit" / "resolved" / ".gitkeep").write_text("")


def generate_wiki(wiki_root: Path, num_pages: int):
    scaffold_wiki(wiki_root)

    all_titles: list[str] = []
    for i in range(num_pages):
        page_type = PAGE_TYPES[i % len(PAGE_TYPES)]
        title = f"{page_type.title()}-{i:04d}"
        all_titles.append(title)
        content = generate_page(title, page_type, all_titles[:i], i)
        dest = wiki_root / "wiki" / page_type / f"{title}.md"
        dest.write_text(content, encoding="utf-8")

    lines = ["# Benchmark Wiki\n"]
    for pt in PAGE_TYPES:
        lines.append(f"## {pt.title()}\n")
        for i in range(num_pages):
            if i % len(PAGE_TYPES) == PAGE_TYPES.index(pt):
                title = f"{pt.title()}-{i:04d}"
                lines.append(f"- [[{title}]]\n")
        lines.append("\n")
    (wiki_root / "wiki" / "index.md").write_text("".join(lines), encoding="utf-8")

    return all_titles


def count_files(wiki_root: Path) -> int:
    return len(list(wiki_root.rglob("*.md")))


def run_lint(wiki_root: Path) -> float:
    start = time.perf_counter()
    subprocess.run(
        [sys.executable, str(LINT_SCRIPT), str(wiki_root), "--json"],
        capture_output=True, text=True, timeout=300,
    )
    return (time.perf_counter() - start) * 1000


def run_graph_build(wiki_root: Path) -> float:
    start = time.perf_counter()
    subprocess.run(
        ["node", str(GRAPH_ENGINE), "--wiki", str(wiki_root), "--action", "build"],
        capture_output=True, text=True, timeout=300,
    )
    return (time.perf_counter() - start) * 1000


def run_graph_insights(wiki_root: Path) -> tuple[float, dict]:
    start = time.perf_counter()
    result = subprocess.run(
        ["node", str(GRAPH_ENGINE), "--wiki", str(wiki_root), "--action", "insights"],
        capture_output=True, text=True, timeout=300,
    )
    elapsed = (time.perf_counter() - start) * 1000
    data = {}
    if result.returncode == 0:
        try:
            data = json.loads(result.stdout)
        except json.JSONDecodeError:
            pass
    return elapsed, data


def run_python_insights(wiki_root: Path) -> tuple[float, dict]:
    start = time.perf_counter()
    result = subprocess.run(
        [sys.executable, str(INSIGHTS_SCRIPT), str(wiki_root), "--format", "json"],
        capture_output=True, text=True, timeout=300,
    )
    elapsed = (time.perf_counter() - start) * 1000
    data = {}
    if result.returncode == 0:
        try:
            data = json.loads(result.stdout)
        except json.JSONDecodeError:
            pass
    return elapsed, data


def read_graph_nodes_and_edges(wiki_root: Path) -> tuple[int, int]:
    graph_data_path = wiki_root / "graph-data.json"
    if not graph_data_path.exists():
        return 0, 0
    try:
        with open(graph_data_path) as f:
            data = json.load(f)
        return len(data.get("nodes", [])), len(data.get("edges", []))
    except (json.JSONDecodeError, IOError):
        return 0, 0


def compute_scaling_factor(times: list[float], sizes: list[int]) -> float:
    n = len(times)
    if n < 2:
        return 0.0
    log_sizes = [math.log(s) for s in sizes]
    log_times = [math.log(max(t, 0.001)) for t in times]
    sum_x = sum(log_sizes)
    sum_y = sum(log_times)
    sum_xy = sum(x * y for x, y in zip(log_sizes, log_times))
    sum_x2 = sum(x * x for x in log_sizes)
    denom = n * sum_x2 - sum_x * sum_x
    if denom == 0:
        return 0.0
    slope = (n * sum_xy - sum_x * sum_y) / denom
    return round(slope, 2)


def write_csv(filepath: str, results: list[dict]):
    fieldnames = ["size", "files_count", "nodes_count", "edges_count",
                  "lint_ms", "build_ms", "insights_ms", "py_insights_ms"]
    with open(filepath, "w", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(results)


def run_benchmarks(output_csv: str) -> int:
    if WORKSPACE.exists():
        shutil.rmtree(WORKSPACE)
    WORKSPACE.mkdir(parents=True)

    results: list[dict] = []

    for size in SIZES:
        print(f"\n{'=' * 60}", file=sys.stderr)
        print(f"Benchmarking size: {size} pages", file=sys.stderr)
        print(f"{'=' * 60}", file=sys.stderr)

        wiki_dir = WORKSPACE / f"wiki-{size}"

        print(f"  Generating {size} pages...", file=sys.stderr)
        generate_wiki(wiki_dir, size)
        files_count = count_files(wiki_dir)
        print(f"  Files: {files_count}", file=sys.stderr)

        print(f"  Running lint...", file=sys.stderr)
        lint_ms = run_lint(wiki_dir)
        print(f"    lint: {lint_ms:.1f} ms", file=sys.stderr)

        print(f"  Running graph build...", file=sys.stderr)
        build_ms = run_graph_build(wiki_dir)
        print(f"    build: {build_ms:.1f} ms", file=sys.stderr)

        print(f"  Running graph insights...", file=sys.stderr)
        insights_ms, insights_data = run_graph_insights(wiki_dir)
        print(f"    insights: {insights_ms:.1f} ms", file=sys.stderr)

        print(f"  Running Python graph insights...", file=sys.stderr)
        pyinsights_ms, pyinsights_data = run_python_insights(wiki_dir)
        print(f"    py-insights: {pyinsights_ms:.1f} ms", file=sys.stderr)

        nodes_count, edges_count = read_graph_nodes_and_edges(wiki_dir)
        if not nodes_count:
            py_summary = pyinsights_data.get("summary", {}) if pyinsights_data else {}
            nodes_count = py_summary.get("nodeCount", 0)
            edges_count = py_summary.get("edgeCount", 0)

        result_row = {
            "size": size,
            "files_count": files_count,
            "nodes_count": nodes_count,
            "edges_count": edges_count,
            "lint_ms": round(lint_ms, 1),
            "build_ms": round(build_ms, 1),
            "insights_ms": round(insights_ms, 1),
            "py_insights_ms": round(pyinsights_ms, 1),
        }
        results.append(result_row)

        print(f"  Result: {result_row}", file=sys.stderr)

    write_csv(output_csv, results)

    print("\n")
    print("size,files_count,nodes_count,edges_count,lint_ms,build_ms,insights_ms,py_insights_ms")
    for r in results:
        print(f"{r['size']},{r['files_count']},{r['nodes_count']},{r['edges_count']},"
              f"{r['lint_ms']},{r['build_ms']},{r['insights_ms']},{r['py_insights_ms']}")

    op_names = ["lint", "graph build", "graph insights", "py insights"]
    op_keys = ["lint_ms", "build_ms", "insights_ms", "py_insights_ms"]
    avgs = [(name, sum(r[key] for r in results) / len(results))
            for name, key in zip(op_names, op_keys)]
    fastest = min(avgs, key=lambda x: x[1])
    slowest = max(avgs, key=lambda x: x[1])

    print(f"\n{'=' * 60}")
    print("SUMMARY")
    print(f"{'=' * 60}")
    print(f"Fastest operation: {fastest[0]} ({fastest[1]:.1f} ms avg)")
    print(f"Slowest operation: {slowest[0]} ({slowest[1]:.1f} ms avg)")

    name_key_pairs = [("lint", "lint_ms"), ("graph build", "build_ms"),
                      ("graph insights", "insights_ms"), ("py insights", "py_insights_ms")]
    for op_name, op_key in name_key_pairs:
        op_times = [r[op_key] for r in results]
        sizes = SIZES[:len(results)]
        factor = compute_scaling_factor(op_times, sizes)
        print(f"Scaling factor for {op_name}: O(n^{factor})")

    if WORKSPACE.exists():
        shutil.rmtree(WORKSPACE)

    return 0


def main() -> int:
    if len(sys.argv) < 2:
        print(f"Usage: python3 {sys.argv[0]} <output-csv>", file=sys.stderr)
        return 2
    output_csv = sys.argv[1]
    return run_benchmarks(output_csv)


if __name__ == "__main__":
    sys.exit(main())
